DROP TABLE IF EXISTS public.masjids CASCADE;
DROP TABLE IF EXISTS prayer_times CASCADE;
DROP TABLE IF EXISTS mosques CASCADE;

CREATE TABLE public.masjids (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    name TEXT NOT NULL,
    name_urdu TEXT,
    location TEXT,
    area TEXT,
    fajr TEXT NOT NULL,
    dhuhr TEXT NOT NULL,
    asr TEXT NOT NULL,
    maghrib TEXT NOT NULL,
    isha TEXT NOT NULL,
    jummah TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.masjids ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Allow public read access" ON public.masjids FOR SELECT USING (true);
CREATE POLICY "Allow authenticated full access" ON public.masjids FOR ALL USING (auth.role() = 'authenticated');

